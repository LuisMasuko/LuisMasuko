# Operadores Aritméticos
n1 = float(input('Um valor: '))
n2 = float(input('Um outro valor: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print('A soma vale {}, \n o produto é {}, \n a divisão é {:.3f}'.format(s, m, d))
print('a divisão inteira é {} \n e a potência é {}'.format(di, e))
